public class Datatype {
	public static void main(String[] args) {
		int num1 = 100000;
		float num2 = 12.34f;
		char ch = 'A';
		String name = "홍길동";
		boolean flag = true;
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(ch);
		System.out.println(name);
		System.out.println(flag);
	}
}
